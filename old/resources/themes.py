def check_theme(sender, data):
    theme = get_value(ThemesCombo)
    if theme == 'Default':
        set_default_theme()
        hide_item(CustomColorNode)
    elif theme == 'Light':
        set_light_theme()
        hide_item(CustomColorNode)
    elif theme == 'Cherry':
        set_cherry_theme()
        hide_item(CustomColorNode)
    elif theme == 'Grey':
        set_grey_theme()
        hide_item(CustomColorNode)
    elif theme == 'Custom [NOT AVAILABLE]':
        # set_custom_theme()
        set_default_theme()
        hide_item(CustomColorNode)
    elif theme is None:
        print('I got None themes to check')
        hide_item(CustomColorNode)
    set_default_style()
    set_settings()
